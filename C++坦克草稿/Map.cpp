/********************************************************
函数功能	:画地图
参数		:关卡
返回值	:无
*********************************************************/
void DrawMap(int level)
{
	for (int y = 0; y < 41; y++)
	{
		for (int x = 0; x < 41; x++)
		{
			//画墙 可以破坏的
			if (m_nMap[level][y][x] == WALL)
			{
				WriteChar(x, y, "", 228);
			}
			//画水
			if (m_nMap[level][y][x] == WATER)
			{
				WriteChar(x, y, "~~", 25);
			}
			//家
			if (m_nMap[level][y][x] == HOME)
			{
				WriteChar(x, y, "★", 4);
			}
			//草  暂时没草
			if (m_nMap[level][y][x] == GRASS)
			{
				WriteChar(x, y, "※", 10);
			}
			//画铁  不能破坏的
			if (m_nMap[level][y][x] == STEEL)
			{
				WriteChar(x, y, "■", 15);
			}
			//画铁  不能破坏的
			if (m_nMap[level][y][x] == FRAME)
			{
				WriteChar(x, y, "■", 15);
			}
		}
	}
}